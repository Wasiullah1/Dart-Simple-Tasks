void main(){

  dynamic value = 5;
  print(value.runtimeType);
  print(value);

  //changing the type to String
  value = "Ali";
  print(value.runtimeType);
  print(value);

  //changing the value to Boolean
  value = true;
  print(value.runtimeType);
  print(value);

  //changing the value to Double
  value = 3.5;
  print(value.runtimeType);
  print(value);

}